//variáveis

// não se pode criar variáveis com palavras reservadas.
// variáveis precicam ter nomes significativos.
// não pode começãr o nome de uma variável com um número, letras minusculas de preferencia.
// nomes de variáveis também não podem conter espaços ou traços.
// utilizamos camelCase
// case sensitive, letras maiusculas e minusculas tem diferença para o motor do javascript.
// não podemos redeclarar variáveis com 'let'
// 'let' e 'var' para variáveis, mas dê preferências para 'let'.

let name00; // declarou a variável.
name00 = 'qualquer valor'; // inicializou a variável.

let name01 = 'João'; // string.
let name02 = 'Maria';
let name03 = "Eduardo";

console.log(name01, 'nasceu em 1984');
console.log('Em 2000', name01 , 'conheceu', name02 , '.');
console.log(name02, 'teve um filho com', name01 , 'em 2015');
